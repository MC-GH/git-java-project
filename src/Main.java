public class Main {
    public static void main(String[] args) {
        System.out.println("Hello world!");

        for (int i = 0; i < 3; i++) {
            System.out.println("Iteratie");
        }
        int i = 0;
        while (i < 4) {
            System.out.println("Nog een iteratie");
            i++;
        }
    }
}
